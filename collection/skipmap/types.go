// Int64MapDesc represents a map based on skip list in desdending order.
type Int64MapDesc struct {
	header *int64NodeDesc
	length int64
}

type int64NodeDesc struct {
	key   int64
	value unsafe.Pointer
	next  []*int64NodeDesc
	mu    sync.Mutex
	flags bitflag
}

func newInt64NodeDesc(key int64, value interface{}, level int) *int64NodeDesc {
	n := &int64NodeDesc{
		key:  key,
		next: make([]*int64NodeDesc, level),
	}
	n.storeVal(value)
	return n
}

func (n *int64NodeDesc) storeVal(value interface{}) {
	atomic.StorePointer(&n.value, unsafe.Pointer(&value))
}

func (n *int64NodeDesc) loadVal() interface{} {
	return *(*interface{})(atomic.LoadPointer(&n.value))
}

// loadNext return `n.next[i]`(atomic)
func (n *int64NodeDesc) loadNext(i int) *int64NodeDesc {
	return (*int64NodeDesc)(atomic.LoadPointer((*unsafe.Pointer)(unsafe.Pointer(&n.next[i]))))
}

// storeNext same with `n.next[i] = value`(atomic)
func (n *int64NodeDesc) storeNext(i int, value *int64NodeDesc) {
	atomic.StorePointer((*unsafe.Pointer)(unsafe.Pointer(&n.next[i])), unsafe.Pointer(value))
}

func (n *int64NodeDesc) lessthan(key int64) bool {
	return n.key > key
}

func (n *int64NodeDesc) equal(key int64) bool {
	return n.key == key
}

// NewInt64Desc return an empty int64 skipmap.
func NewInt64Desc() *Int64MapDesc {
	h := newInt64NodeDesc(0, "", maxLevel)
	h.flags.SetTrue(fullyLinked)
	return &Int64MapDesc{
		header: h,
	}
}

// findNode takes a key and two maximal-height arrays then searches exactly as in a sequential skipmap.
// The returned preds and succs always satisfy preds[i] > key >= succs[i].
// (without fullpath, if find the node will return immediately)
func (s *Int64MapDesc) findNode(key int64, preds *[maxLevel]*int64NodeDesc, succs *[maxLevel]*int64NodeDesc) *int64NodeDesc {
	x := s.header
	for i := maxLevel - 1; i >= 0; i-- {
		succ := x.loadNext(i)
		for succ != nil && succ.lessthan(key) {
			x = succ
			succ = x.loadNext(i)
		}
		preds[i] = x
		succs[i] = succ

		// Check if the key already in the skipmap.
		if succ != nil && succ.equal(key) {
			return succ
		}
	}
	return nil
}

// findNodeDelete takes a key and two maximal-height arrays then searches exactly as in a sequential skip-list.
// The returned preds and succs always satisfy preds[i] > key >= succs[i].
func (s *Int64MapDesc) findNodeDelete(key int64, preds *[maxLevel]*int64NodeDesc, succs *[maxLevel]*int64NodeDesc) int {
	// lFound represents the index of the first layer at which it found a node.
	lFound, x := -1, s.header
	for i := maxLevel - 1; i >= 0; i-- {
		succ := x.loadNext(i)
		for succ != nil && succ.lessthan(key) {
			x = succ
			succ = x.loadNext(i)
		}
		preds[i] = x
		succs[i] = succ

		// Check if the key already in the skip list.
		if lFound == -1 && succ != nil && succ.equal(key) {
			lFound = i
		}
	}
	return lFound
}

func unlockInt64Desc(preds [maxLevel]*int64NodeDesc, highestLevel int) {
	var prevPred *int64NodeDesc
	for i := highestLevel; i >= 0; i-- {
		if preds[i] != prevPred { // the node could be unlocked by previous loop
			preds[i].mu.Unlock()
			prevPred = preds[i]
		}
	}
}

// Store sets the value for a key.
func (s *Int64MapDesc) Store(key int64, value interface{}) {
	level := randomLevel()
	var preds, succs [maxLevel]*int64NodeDesc
	for {
		nodeFound := s.findNode(key, &preds, &succs)
		if nodeFound != nil { // indicating the key is already in the skip-list
			if !nodeFound.flags.Get(marked) {
				// We don't need to care about whether or not the node is fully linked,
				// just replace the value.
				nodeFound.storeVal(value)
				return
			}
			// If the node is marked, represents some other goroutines is in the process of deleting this node,
			// we need to add this node in next loop.
			continue
		}

		// Add this node into skip list.
		var (
			highestLocked        = -1 // the highest level being locked by this process
			valid                = true
			pred, succ, prevPred *int64NodeDesc
		)
		for layer := 0; valid && layer < level; layer++ {
			pred = preds[layer]   // target node's previous node
			succ = succs[layer]   // target node's next node
			if pred != prevPred { // the node in this layer could be locked by previous loop
				pred.mu.Lock()
				highestLocked = layer
				prevPred = pred
			}
			// valid check if there is another node has inserted into the skip list in this layer during this process.
			// It is valid if:
			// 1. The previous node and next node both are not marked.
			// 2. The previous node's next node is succ in this layer.
			valid = !pred.flags.Get(marked) && (succ == nil || !succ.flags.Get(marked)) && pred.next[layer] == succ
		}
		if !valid {
			unlockInt64Desc(preds, highestLocked)
			continue
		}

		nn := newInt64NodeDesc(key, value, level)
		for layer := 0; layer < level; layer++ {
			nn.next[layer] = succs[layer]
			preds[layer].storeNext(layer, nn)
		}
		nn.flags.SetTrue(fullyLinked)
		unlockInt64Desc(preds, highestLocked)
		atomic.AddInt64(&s.length, 1)
	}
}

